public class Person {
    private String p_name;
    private String p_email;

    public Person() {
        this.p_name ="";
        this.p_email="";
    }

    public String getP_name() {
        return p_name;
    }

    public void setP_name(String p_name) {
        this.p_name = p_name;
    }

    public String getP_email() {
        return p_email;
    }

    public void setP_email(String p_email) {
        this.p_email = p_email;
    }
}
